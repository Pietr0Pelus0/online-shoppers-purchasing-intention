def build_categorical_summary(
    df: pd.DataFrame,
    features: list,
    target_col: str = "Revenue"
) -> dict[str, pd.DataFrame]:
    """
    Computes the count, percentage, and conversion rate
    for each categorical feature in a single vectorized operation.

    Returns
    -------
    dict : {feature_name: summary DataFrame}
    """
    summaries = {}
    for feature in features:
        counts     = df[feature].value_counts()
        pct        = counts / len(df) * 100
        conversion = df.groupby(feature)[target_col].mean() * 100

        summaries[feature] = (
            pd.DataFrame({
                "Count":             counts,
                "Percentage (%)":       pct.round(2),
                "Conversion Rate (%)": conversion.round(2)
            })
            .sort_values("Count", ascending=False)
        )
    return summaries


def plot_conversion_rates(
    summaries: dict[str, pd.DataFrame],
    n_cols: int = 2
) -> None:
    """
    Visualizes the conversion rate for each categorical feature
    in a compact subplot grid.

    Parameters
    ----------
    summaries : dict returned by build_categorical_summary()
    n_cols    : number of columns in the grid (default 2)
    """
    features = list(summaries.keys())
    n_rows   = -(-len(features) // n_cols)

    fig, axes = plt.subplots(n_rows, n_cols,
                             figsize=(14, n_rows * 4),
                             constrained_layout=True)
    axes = axes.flatten()

    for ax, feature in zip(axes, features):
        conv_sorted = summaries[feature]["Conversion Rate (%)"].sort_values(ascending=False)

        sns.barplot(
            x=conv_sorted.index.astype(str),
            y=conv_sorted.values,
            palette="Greens_d",
            ax=ax
        )

        ax.bar_label(ax.containers[0], fmt="%.1f%%", padding=3, fontsize=8)
        ax.set_title(feature, fontsize=11, fontweight="bold")
        ax.set_xlabel("")
        ax.set_ylabel("Conversion Rate (%)", fontsize=9)
        ax.tick_params(axis="x", rotation=45)

    for ax in axes[len(features):]:
        fig.delaxes(ax)

    # fig.suptitle(
    #     "Conversion Rate (%) by Categorical Feature",
    #     fontsize=13, fontweight="bold"
    # )
    plt.show()


