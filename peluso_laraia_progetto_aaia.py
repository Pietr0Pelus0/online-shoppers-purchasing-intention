def compute_ga_stats_by_class(
    df: pd.DataFrame,
    metrics: list,
    target_col: str = "Revenue"
) -> pd.DataFrame:
    """
    Computes the mean, median, and std of the GA metrics for each Revenue class.
    Adds the percentage variation between the classes (Δ%).

    Returns
    -------
    pd.DataFrame with MultiIndex (Metric, Statistic) per Revenue column.
    """
    stats = (
        df.groupby(target_col)[metrics]
        .agg(["mean", "median", "std"])
        .T.round(4)
    )
    stats.index.names = ["Metric", "Statistic"]

    # Δ% between the True and False classes on the mean
    means = df.groupby(target_col)[metrics].mean()
    delta = ((means.loc[True] - means.loc[False]) /
              means.loc[False].replace(0, np.nan) * 100).round(1)

    print("\n=== GA METRICS STATISTICS BY REVENUE CLASS ===")
    print(stats.to_string())
    print("\n── Δ% Mean (True vs False) ─────────────────────")
    print(delta.to_string())

    return stats

def plot_ga_violins(
    df: pd.DataFrame,
    metrics: list,
    target_col: str = "Revenue"
) -> None:
    """
    Split violin plot for each GA metric.
    The left half represents 'No Revenue' (False),
    the right half 'Revenue' (True).
    One subplot per metric, separate figure.

    Note: split=True (hue_order with 2 levels) requires Seaborn >= 0.11.
    On Seaborn >= 0.13 the parameter is called 'split'; on some intermediate
    versions it is 'inner_kws'. The signature below is compatible with 0.11-0.13.
    """
    plot_df = df[metrics + [target_col]].copy()
    plot_df[target_col] = plot_df[target_col].map({False: "No Revenue", True: "Revenue"})

    palette    = {"No Revenue": "#5B9BD5", "Revenue": "#ED7D31"}
    hue_order  = ["No Revenue", "Revenue"]

    fig, axes = plt.subplots(1, len(metrics), figsize=(15, 6))

    for ax, metric in zip(axes, metrics):
        # 'x' is the metric itself: we use a dummy axis to center
        # the violin, so each subplot shows a single feature.
        sns.violinplot(
            data=plot_df,
            x=np.zeros(len(plot_df)),
            y=metric,
            hue=target_col,
            hue_order=hue_order,
            split=True,
            palette=palette,
            inner="quartile",           # show inner Q1, median, Q3
            linewidth=0.8,
            ax=ax
        )
        ax.set_title(metric, fontweight="bold", fontsize=11)
        ax.set_xlabel("")
        ax.set_ylabel(metric, fontsize=9)
        ax.set_xticks([])               # hides the dummy x-axis

        # Log scale for PageValues: many zeros → symlog to avoid losing the 0
        if metric == "PageValues":
            ax.set_yscale("symlog")
            ax.set_ylabel("PageValues (symlog scale)", fontsize=9)

        # Legend only on the first subplot to avoid repeating it
        if ax != axes[0]:
            ax.get_legend().remove()
        else:
            ax.legend(title="Revenue", fontsize=8, title_fontsize=9,
                      loc="upper right")

    # fig.suptitle(
