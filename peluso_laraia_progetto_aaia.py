def compute_global_correlation(
    df: pd.DataFrame,
    features: list,
    target_col: str = "Revenue"
) -> pd.DataFrame:
    """
    Computes the Pearson correlation matrix over all numerical
    features + target (encoded as 0/1).

    Identifies and prints the pairs with |r| > 0.70 as candidates
    for informational redundancy.

    Returns
    -------
    pd.DataFrame : complete correlation matrix.
    """
    # Adds the numerical target to evaluate its linear correlation
    analysis_df = df[features].copy()
    analysis_df[target_col] = df[target_col].astype(int)

    corr = analysis_df.corr(method="pearson").round(4)

    # ── Heatmap ──────────────────────────────────────────────────
    fig, ax = plt.subplots(figsize=(13, 10))
    mask = np.triu(np.ones_like(corr, dtype=bool), k=1)   # Shows only the lower triangle

    sns.heatmap(
        corr, mask=mask,
        annot=True, fmt=".2f", cmap="coolwarm",
        center=0, vmin=-1, vmax=1,
        linewidths=0.4, linecolor="white",
        cbar_kws={"label": "Pearson r"},
        ax=ax
    )
    # ax.set_title("Task 1.5 – Correlation Matrix: Numerical Features",
    #              fontsize=13, fontweight="bold")
    plt.tight_layout()
    plt.show()

    # ── High-correlation pairs ───────────────────────────────
    corr_unstacked = (
        corr.where(np.tril(np.ones_like(corr, dtype=bool), k=-1))
        .stack()
        .reset_index()
        .rename(columns={"level_0": "Feature A", "level_1": "Feature B", 0: "Pearson r"})
        .assign(abs_r=lambda x: x["Pearson r"].abs())
        .query("abs_r > 0.70")
        .sort_values("abs_r", ascending=False)
        .assign(Level=lambda x: np.where(x["abs_r"] > 0.85, "Very High", "High"))
    )

    print("\n=== PAIRS AT RISK OF INFORMATION REDUNDANCY (|r| > 0.70) ===")
    if corr_unstacked.empty:
        print("  No pair exceeds the threshold |r| > 0.70.")
    else:
        print(corr_unstacked[["Feature A", "Feature B", "Pearson r", "Level"]]
              .to_string(index=False))

    return corr


