def compute_descriptive_stats(df: pd.DataFrame, features: list) -> pd.DataFrame:
    """
    Computes descriptive statistics for the numerical features and adds
    the Coefficient of Variation (CV) as an indicator of heterogeneity.

    Returns
    -------
    pd.DataFrame
        Table with Mean, Median, Std, Min, Q1, Q3, Max, CV(%) and Skewness.
    """

    df_subset = df[features]

    # describe() already provides count, mean, std, min, quartiles, max
    # we integrate it with median, skewness and CV in a single vectorized operation
    stats_df = df_subset.describe().T.drop(columns="count")
    stats_df.columns = ["Mean", "Std Dev", "Min", "Q1 (25%)", "Median (50%)", "Q3 (75%)", "Max"]

    stats_df["CV (%)"]   = (stats_df["Std Dev"] / stats_df["Mean"].replace(0, np.nan) * 100).round(2)
    stats_df["Skewness"] = df_subset.skew()

    print("\n=== DESCRIPTIVE STATISTICS – NUMERICAL FEATURES ===\n")
    print(stats_df.to_string())

    return stats_df


