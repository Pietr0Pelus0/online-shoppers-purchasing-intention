def evaluate_and_log_model(
    model,
    X_test,
    y_test,
    model_name  : str   = "Model",
    oob_score   : float = None,
    baseline_metrics: dict = None,
) -> dict:
    """
    Evaluates a classifier on the test set and prints the report
    in the project's standardized format.

    Parameters
    ----------
    model            : trained sklearn-compatible classifier
    X_test           : test DataFrame
    y_test           : test target Series
    model_name       : identifying string (used in the header)
    oob_score        : if available (RF with oob_score=True),
                       it is included in the report
    baseline_metrics : dict returned by a previous call
                       (used in the Tuned blocks to print the Δ)

    Returns
    -------
    dict with keys:
        roc_auc, pr_auc, y_pred (array), y_proba (array), model_name
    """
    y_pred  = model.predict(X_test)
    y_proba = model.predict_proba(X_test)[:, 1]

    roc_auc = roc_auc_score(y_test, y_proba)
    pr_auc  = average_precision_score(y_test, y_proba)
    report  = classification_report(
        y_test, y_pred,
        target_names=["No Revenue (0)", "   Revenue (1)"],
    )

    sep = "=" * 60

    # ── Header ──────────────────────────────────────────────────
    print(f"\n{sep}")
    print(f"  {model_name.upper()} – TEST SET RESULTS")
    print(sep)

    # ── Main metrics ──────────────────────────────────────
    if oob_score is not None:
        print(f"\n  OOB Score (internal train) : {oob_score:.4f}")

    if baseline_metrics is None:
        # Baseline Block: absolute print
        print(f"  ROC-AUC    (test)          : {roc_auc:.4f}")
        print(f"  PR-AUC / AP(test)          : {pr_auc:.4f}")
    else:
        # Tuned Block: print with delta vs baseline
        delta_roc = roc_auc - baseline_metrics["roc_auc"]
        delta_pr  = pr_auc  - baseline_metrics["pr_auc"]
        print(f"  ROC-AUC  : {roc_auc:.4f}"
              f"  (Δ vs baseline: {delta_roc:+.4f})")
        print(f"  PR-AUC   : {pr_auc:.4f}"
              f"  (Δ vs baseline: {delta_pr:+.4f})")

    # ── Classification Report ────────────────────────────────────
    print("\n── Classification Report " + "─" * 35)
    print(report)
    print(sep)

    return {
        "model_name": model_name,
        "roc_auc"   : roc_auc,
        "pr_auc"    : pr_auc,
        "y_pred"    : y_pred,
        "y_proba"   : y_proba,
    }

