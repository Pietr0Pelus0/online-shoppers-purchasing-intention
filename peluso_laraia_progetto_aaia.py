# ------------------------------------------------------------
# BLOCK 3.3: FEATURE SELECTION (RELEVANCE VS REDUNDANCY FILTERS)
# ------------------------------------------------------------
# This block implements a two-stage selection process:
# 1. Relevance Filter: Removes features with MI below a minimum threshold.
# 2. Redundancy Filter: Identifies highly correlated pairs and,
#    using MI as the "arbiter", removes the less informative variable.
# ------------------------------------------------------------

def select_features_hybrid(
    X_train: pd.DataFrame,
    mi_df: pd.DataFrame,
    mi_threshold: float = 0.002,
    corr_threshold: float = 0.80
) -> tuple[pd.DataFrame, list, list]:
    """
    Selects features by combining Mutual Information and Linear Correlation.

    Parameters
    ----------
    X_train : pd.DataFrame
        Training DataFrame (scaled).
    mi_df : pd.DataFrame
        DataFrame with the Mutual Information scores.
    mi_threshold : float
        Minimum MI threshold below which the feature is discarded.
    corr_threshold : float
        Linear correlation (Pearson) threshold above which redundancy is detected.

    Returns
    -------
    X_selected : pd.DataFrame
        DataFrame containing only the selected features.
    low_mi_removed : list
        List of features removed due to low relevance.
    redundant_removed : list
        List of features removed due to redundancy.
    """
    # Quick map Feature -> MI Score
    mi_map = dict(zip(mi_df["Feature"], mi_df["MI_Score"]))

    # --- Relevance Filter (Low MI) ---
    low_mi_removed = [feat for feat, score in mi_map.items() if score < mi_threshold]
    features_relevant = [feat for feat in X_train.columns if feat not in low_mi_removed]

    # --- Redundancy Filter (High Correlation) ---
    X_ref = X_train[features_relevant]
    corr_matrix = X_ref.corr().abs()

    # Extract the upper triangle to avoid duplicates
    upper_tri = corr_matrix.where(np.triu(np.ones(corr_matrix.shape), k=1).astype(bool))

    redundant_removed = set()

    # Find the correlated pairs and apply MI-based elimination
    for col in upper_tri.columns:
        for row in upper_tri.index:
            if upper_tri.loc[row, col] > corr_threshold:
                # We have a correlated pair (row, col)
                if row not in redundant_removed and col not in redundant_removed:
                    mi_row = mi_map.get(row, 0.0)
                    mi_col = mi_map.get(col, 0.0)

                    # We keep the feature with the highest MI, we drop the other
                    if mi_row < mi_col:
                        redundant_removed.add(row)
                    else:
                        redundant_removed.add(col)

    redundant_removed = list(redundant_removed)

    # Final features to keep
    features_final = [f for f in features_relevant if f not in redundant_removed]

    return X_train[features_final], low_mi_removed, redundant_removed


def print_selection_report(
    removed_low_mi: list,
    removed_red: list,
    features_final: list,
    mi_map: dict
) -> None:
    """
    Prints a structured report of the feature selection process.
    Shows removed features (with their MI score) and the final retained set.

    Parameters
    ----------
    removed_low_mi  : features dropped by the relevance filter
    removed_red     : features dropped by the redundancy filter
    features_final  : features retained after both filters
    mi_map          : dict mapping Feature -> MI Score
    """

    # ── Relevance Filter Report ──────────────────────────────────
    # Lists all features whose MI score fell below mi_threshold.
    # A low MI score means the feature carries negligible information
    # about the target, regardless of its correlation with other features.
    print("=" * 60)
    print("FEATURE SELECTION REPORT")
    print("=" * 60)

    print(f"\n[1] Relevance Filter  (MI < {MI_THRESHOLD_SEL})")
    print(f"    Removed: {len(removed_low_mi)} feature(s)")
    if removed_low_mi:
        for feat in sorted(removed_low_mi, key=lambda f: mi_map.get(f, 0)):
            print(f"    ✗  {feat:<35}  MI = {mi_map.get(feat, 0):.5f}")
    else:
        print("    No features removed.")

    # ── Redundancy Filter Report ─────────────────────────────────
    # Lists features removed because they exceeded the Pearson
    # correlation threshold with a more informative counterpart.
    # The retained feature in each pair is the one with higher MI.
    print(f"\n[2] Redundancy Filter  (|r| > {CORR_THRESHOLD_SEL})")
    print(f"    Removed: {len(removed_red)} feature(s)")
    if removed_red:
        for feat in sorted(removed_red, key=lambda f: mi_map.get(f, 0)):
            print(f"    ✗  {feat:<35}  MI = {mi_map.get(feat, 0):.5f}")
    else:
        print("    No features removed.")

    # ── Final Feature Set ────────────────────────────────────────
    # The retained features passed both filters:
    # sufficient relevance (MI) and no redundancy with a better feature.
    print(f"\n[3] Retained Features: {len(features_final)}")
    for feat in features_final:
        print(f"    ✓  {feat:<35}  MI = {mi_map.get(feat, 0):.5f}")

    print("=" * 60)

