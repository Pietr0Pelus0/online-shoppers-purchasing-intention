def build_specialday_summary(df: pd.DataFrame) -> pd.DataFrame:
    """
    Aggregate by SpecialDay level: session volume, mean PageValues
    and conversion rate.

    Returns
    -------
    pd.DataFrame indexed on the SpecialDay value.
    """
    return (
        df.groupby("SpecialDay")
        .agg(
            N_Sessioni   = ("Revenue",    "count"),
            PageVal_Medio= ("PageValues", "mean"),
            Conv_Rate    = ("Revenue",    "mean")
        )
        .assign(
            PageVal_Medio = lambda x: x["PageVal_Medio"].round(2),
            Conv_Rate     = lambda x: (x["Conv_Rate"] * 100).round(2)
        )
        .rename(columns={
            "N_Sessioni":    "N. Sessions",
            "PageVal_Medio": "Mean PageValues",
            "Conv_Rate":     "Conv. Rate (%)"
        })
    )


def build_month_specialday_pivot(df: pd.DataFrame) -> pd.DataFrame:
    """
    Builds a Month × SpecialDay pivot with the mean conversion rate.
    Sorts the months according to MONTH_ORDER.
    """
    pivot = (
        df.groupby(["Month", "SpecialDay"])["Revenue"]
        .mean()
        .mul(100).round(2)
        .unstack(fill_value=0)
    )
    present_months = [m for m in MONTH_ORDER if m in pivot.index]
    return pivot.loc[present_months]


