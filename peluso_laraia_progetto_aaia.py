def fit_and_profile(X_pca, df_encoded, target_col,
                    numeric_cols, dummy_cols, k, random_state):
    """
    Run K-Means for a given k and return:
    - labels     : array of cluster labels
    - num_prof   : mean numeric profile per cluster
    - dummy_prof : categorical profile (dummies with prevalence > 5%)
    - meta       : conversion rate and size per cluster
    """
    km     = KMeans(n_clusters=k, random_state=random_state, n_init=10)
    labels = km.fit_predict(X_pca)

    df_prof               = df_encoded.copy()
