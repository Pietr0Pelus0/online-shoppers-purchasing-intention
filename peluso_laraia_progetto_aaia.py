VARIANCE_THRESHOLD_PCA = 0.9   # Cumulative variance to preserve
RANDOM_STATE = 42

def fit_pca(df_scaled: pd.DataFrame,
            target_col: str,
            variance_threshold: float = 0.95) -> tuple:
    """
    Fits PCA on the scaled dataset (excluding the target) and automatically
    selects the number of components that explain at least
    `variance_threshold` of the total variance.

    Returns
    -------
    X_pca        : array (n_samples, n_components) – PCA coordinates
    pca          : fitted PCA object
    n_components : number of selected components
    """
    X = df_scaled.drop(columns=[target_col]).values

    # First fit with all components for variance analysis
    pca_full = PCA(random_state=RANDOM_STATE)
    pca_full.fit(X)

    cumvar = np.cumsum(pca_full.explained_variance_ratio_)
    n_components = int(np.searchsorted(cumvar, variance_threshold) + 1)

    print(f"Components selected for {variance_threshold*100:.0f}% variance: {n_components}")
    print(f"Effective explained variance: {cumvar[n_components-1]*100:.2f}%")

    # Second fit with the optimal number of components
    pca = PCA(n_components=n_components, random_state=RANDOM_STATE)
    X_pca = pca.fit_transform(X)

    return X_pca, pca, n_components, pca_full


