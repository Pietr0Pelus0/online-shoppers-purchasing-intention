def plot_numeric_distributions(
    df: pd.DataFrame,
    features: list,
    target_col: str = "Revenue"
) -> None:
    """
    Visualizes the distribution of numerical features through KDE computed
    on log1p-transformed data,
    separating the sessions by target class (Revenue True/False).

    The X-axis ticks are re-labeled with the original values
    through the inverse expm1 transformation.
    """
    palette = {False: "#4C72B0", True: "#DD8452"}
    labels  = {False: "No Revenue", True: "Revenue"}

    n_cols  = 2
    n_rows  = -(-len(features) // n_cols)

    fig, axes = plt.subplots(n_rows, n_cols,
                              figsize=(14, n_rows * 4),
                              constrained_layout=True)
    axes = axes.flatten()

    for ax, feature in zip(axes, features):
        for rev_val, color in palette.items():
            data_log = np.log1p(df.loc[df[target_col] == rev_val, feature])
            sns.kdeplot(data_log, ax=ax, color=color,
                        label=labels[rev_val], fill=True,
                        alpha=0.35, linewidth=1.8)
            sns.rugplot(data_log, ax=ax, color=color,
                        alpha=0.08, height=0.06)

        # Re-label the ticks with the original values (inverse log1p)
        log_ticks = ax.get_xticks()
        ax.set_xticks(log_ticks)
        ax.set_xticklabels(
            [f"{np.expm1(t):.4g}" for t in log_ticks],
            fontsize=7, rotation=30
        )
        ax.set_title(feature, fontsize=11, fontweight="bold")
        ax.set_xlabel("Original value (log1p scale)", fontsize=8)
        ax.set_ylabel("Density", fontsize=9)
        ax.legend(fontsize=8)

    for ax in axes[len(features):]:
        fig.delaxes(ax)

    # fig.suptitle(
    #     "Numerical Feature Distributions by Revenue Class (KDE log1p)",
    #     fontsize=13, fontweight="bold"
    # )
    plt.show()


plot_numeric_distributions(df, ALL_NUMERIC_FEATURES, TARGET_COL)

